package com.neocoretechs.arieslogger.core.impl;

public class DirectoryExistsException extends Exception {

	public DirectoryExistsException() {

	}

	public DirectoryExistsException(String arg0) {
		super(arg0);
	
	}

	public DirectoryExistsException(Throwable arg0) {
		super(arg0);
	
	}

	public DirectoryExistsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	
	}

	public DirectoryExistsException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);

	}

}
